package TicketPricingSystem;

public class Journey {
    private int daysToJourney;

    // Constructor, Getters, and Setters
    public Journey(int daysToJourney) {
        this.daysToJourney = daysToJourney;
    }

    public int getDaysToJourney() {
        return daysToJourney;
    }

    public void setDaysToJourney(int daysToJourney) {
        this.daysToJourney = daysToJourney;
    }
}
